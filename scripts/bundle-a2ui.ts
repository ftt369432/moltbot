import { createHash } from "node:crypto";
import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { execSync } from "node:child_process";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT_DIR = path.resolve(__dirname, "..");

const HASH_FILE = path.join(ROOT_DIR, "src/canvas-host/a2ui/.bundle.hash");
const OUTPUT_FILE = path.join(ROOT_DIR, "src/canvas-host/a2ui/a2ui.bundle.js");
const A2UI_RENDERER_DIR = path.join(ROOT_DIR, "vendor/a2ui/renderers/lit");
const A2UI_APP_DIR = path.join(ROOT_DIR, "apps/shared/OpenClawKit/Tools/CanvasA2UI");

const INPUT_PATHS = [
  path.join(ROOT_DIR, "package.json"),
  path.join(ROOT_DIR, "pnpm-lock.yaml"),
  A2UI_RENDERER_DIR,
  A2UI_APP_DIR,
];

async function exists(p: string) {
  try {
    await fs.access(p);
    return true;
  } catch {
    return false;
  }
}

async function walk(entryPath: string, files: string[]) {
  const st = await fs.stat(entryPath);
  if (st.isDirectory()) {
    const entries = await fs.readdir(entryPath);
    for (const entry of entries) {
      await walk(path.join(entryPath, entry), files);
    }
  } else {
    files.push(entryPath);
  }
}

function normalize(p: string) {
  return p.split(path.sep).join("/");
}

async function computeHash() {
  const files: string[] = [];
  for (const input of INPUT_PATHS) {
    if (await exists(input)) {
        await walk(input, files);
    }
  }

  files.sort((a, b) => normalize(a).localeCompare(normalize(b)));

  const hash = createHash("sha256");
  for (const filePath of files) {
    const rel = normalize(path.relative(ROOT_DIR, filePath));
    hash.update(rel);
    hash.update("\0");
    const content = await fs.readFile(filePath);
    hash.update(content);
    hash.update("\0");
  }

  return hash.digest("hex");
}

async function main() {
  // Docker builds exclude vendor/apps via .dockerignore.
  // In that environment we must keep the prebuilt bundle.
  if (!(await exists(A2UI_RENDERER_DIR)) || !(await exists(A2UI_APP_DIR))) {
    console.log("A2UI sources missing; keeping prebuilt bundle.");
    process.exit(0);
  }

  const currentHash = await computeHash();
  let previousHash = "";

  if (await exists(HASH_FILE)) {
    previousHash = (await fs.readFile(HASH_FILE, "utf-8")).trim();
    if (previousHash === currentHash && (await exists(OUTPUT_FILE))) {
      console.log("A2UI bundle up to date; skipping.");
      process.exit(0);
    }
  }

  console.log("Bundling A2UI...");
  
  try {
     // Use pnpm exec to run local binaries
    execSync(`pnpm exec tsc -p "${A2UI_RENDERER_DIR}/tsconfig.json"`, { stdio: "inherit", cwd: ROOT_DIR });
    
    // Rolldown
    // We need to run rolldown command. 
    // The original script ran: rolldown -c "$A2UI_APP_DIR/rolldown.config.mjs"
    // We should make sure we are in the root dir or adjust paths accordingly.
    execSync(`pnpm exec rolldown -c "${A2UI_APP_DIR}/rolldown.config.mjs"`, { stdio: "inherit", cwd: ROOT_DIR });

    await fs.writeFile(HASH_FILE, currentHash);
    console.log("A2UI bundle success.");
  } catch (error) {
    console.error("A2UI bundling failed.");
    console.error(error);
    process.exit(1);
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
